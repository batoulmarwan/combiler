    public String getProductClick_body() {
        return ProductClick_body;
    }

    public void setProductClick_body(String productClick_body) {
        ProductClick_body = productClick_body;
    }

    public String getProductClick_Close() {
        return ProductClick_Close;
    }

    public void setProductClick_Close(String productClick_Close) {
        ProductClick_Close = productClick_Close;
    }
